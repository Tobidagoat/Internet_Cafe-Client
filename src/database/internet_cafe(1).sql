    -- Recalculate the total_price for the affected order_id
    UPDATE food_order
    SET total_food_price = (
        SELECT COALESCE(SUM(fod.qty * f.price), 0)
        FROM food_order_detail fod
        JOIN foods f ON fod.food_id = f.food_id
        WHERE fod.order_id = NEW.order_id -- Only sum for the order that was just updated
    )
    WHERE order_id = NEW.order_id;
END
